class SimpleExpressions{

  public static void main(String[] args){
    System.out.println(("19-character string").length()); //length of string.
    System.out.println(("abcd").charAt(3));// get a character.
    System.out.println(("abcd").equals("abcd"));// whether they are equal.
    System.out.println(("Hello, world!").substring(7,12));// substring from position a to the position before b.
    System.out.println(("Hello, world!").substring(7));// substrng from position a to the end.

  }
}
