class SpecialStrings{

  public static void main(String[] args){
    System.out.println("Simple string.");
    System.out.println("Say \"Hello.\""); // \" is a double quote.
    System.out.println("Name: \tJohn"); // \t is tab.
    System.out.println("\\Food\\Bar"); // \\ is \.
    System.out.println("One thing\nThe other thing"); // \n is the next line.
  }
}
